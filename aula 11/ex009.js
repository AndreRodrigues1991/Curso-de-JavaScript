var pais = 'EUA'

console.log(`Vivendo em ${pais}`) // vivendo em EUA

if (pais == 'brasil') { // Se o pais for igual Brasil
    console.log('Você é Brasileiro!') // vc é Brasileiro
} else { // Se não
    console.log('Você é Estrangeiro!') // Você é Estrangeiro
}

// Se o pais for Brasil você é Brasileiro! se não você é Estrangeiro!